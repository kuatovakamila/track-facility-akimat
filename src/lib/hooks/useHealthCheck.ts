import { useState, useEffect, useCallback, useRef } from "react";
import { useNavigate } from "react-router-dom";
import { io, type Socket } from "socket.io-client";
import { StateKey } from "../constants";
import toast from "react-hot-toast";

// Constants
const MAX_STABILITY_TIME = 7;
const SOCKET_TIMEOUT = 15000;
const TIMEOUT_MESSAGE = "–ù–µ —É–¥–∞–µ—Ç—Å—è –æ—Ç—Å–ª–µ–¥–∏—Ç—å –¥–∞–Ω–Ω—ã–µ, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑ –∏–ª–∏ —Å–≤—è–∂–∏—Ç–µ—Å—å —Å –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ü–∏–µ–π.";

type SensorData = {
    temperature?: string;
    alcoholLevel?: string;
    cameraStatus?: 'failed' | 'success';
};

type HealthCheckState = {
    currentState: StateKey;
    stabilityTime: number;
    temperatureData: { temperature: number };
    alcoholData: { alcoholLevel: string };
    secondsLeft: number;
};

const STATE_SEQUENCE: StateKey[] = ["TEMPERATURE", "ALCOHOL"];

const configureSocketListeners = (
    socket: Socket,
    currentState: StateKey,
    handlers: {
        onData: (data: SensorData) => void;
        onError: () => void;
    }
) => {
    // ‚úÖ REMOVE PREVIOUS LISTENERS BEFORE ADDING NEW ONES
    socket.off("temperature");
    socket.off("alcohol");
    socket.off("camera");

    if (currentState === "TEMPERATURE") {
        socket.on("temperature", handlers.onData);
    } else if (currentState === "ALCOHOL") {
        socket.on("alcohol", handlers.onData);
    }

    socket.on("camera", handlers.onData);
};

export const useHealthCheck = (): HealthCheckState & {
    handleComplete: () => Promise<void>;
    setCurrentState: React.Dispatch<React.SetStateAction<StateKey>>;
} => {
    const navigate = useNavigate();
    const [state, setState] = useState<HealthCheckState>({
        currentState: "TEMPERATURE",
        stabilityTime: 0,
        temperatureData: { temperature: 0 },
        alcoholData: { alcoholLevel: "–ù–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ" },
        secondsLeft: 15,
    });

    const refs = useRef({
        socket: null as Socket | null,
        timeout: null as NodeJS.Timeout | null,
        lastDataTime: Date.now(),
        hasTimedOut: false,
        isSubmitting: false,
        hasNavigated: false, // ‚úÖ NEW FLAG TO PREVENT RESET AFTER NAVIGATION
    }).current;

    const updateState = useCallback(
        <K extends keyof HealthCheckState>(updates: Pick<HealthCheckState, K>) => {
            setState((prev) => ({ ...prev, ...updates }));
        },
        []
    );

    const handleTimeout = useCallback(() => {
        if (refs.hasTimedOut) return;
        refs.hasTimedOut = true;

        toast.error(TIMEOUT_MESSAGE, {
            duration: 3000,
            style: { background: "#272727", color: "#fff", borderRadius: "8px" },
        });
        navigate("/");
    }, [navigate]);

    const handleDataEvent = useCallback(
        (data: SensorData) => {
            if (!data) {
                console.warn("‚ö†Ô∏è Received empty data packet");
                return;
            }

            console.log("üì° Sensor data received:", data);
            refs.lastDataTime = Date.now();
            clearTimeout(refs.timeout!);
            refs.timeout = setTimeout(handleTimeout, SOCKET_TIMEOUT);

            let alcoholStatus = "–ù–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ";
            if (data.alcoholLevel) {
                alcoholStatus = data.alcoholLevel === "normal" ? "–¢—Ä–µ–∑–≤—ã–π" : "–ü—å—è–Ω—ã–π";
            }

            setState((prev) => {
                if (prev.currentState === "ALCOHOL") {
                    console.log("‚úÖ Alcohol data received, completing progress.");
                    return {
                        ...prev,
                        stabilityTime: MAX_STABILITY_TIME,
                        alcoholData: { alcoholLevel: alcoholStatus },
                    };
                }

                return {
                    ...prev,
                    stabilityTime: prev.currentState === "TEMPERATURE"
                        ? Math.min(prev.stabilityTime + 1, MAX_STABILITY_TIME)
                        : prev.stabilityTime,
                    temperatureData: prev.currentState === "TEMPERATURE"
                        ? { temperature: Number(data.temperature) || 0 }
                        : prev.temperatureData,
                };
            });

            if (state.currentState === "ALCOHOL") {
                setTimeout(handleComplete, 300);
            }
        },
        [handleTimeout]
    );

    useEffect(() => {
        if (!refs.socket) {
            refs.socket = io(import.meta.env.VITE_SERVER_URL, {
                transports: ["websocket"],
                reconnection: true,
                reconnectionAttempts: 20,
                reconnectionDelay: 10000,
            });

            refs.socket.on("connect", () => {
                console.log("‚úÖ WebSocket connected.");
            });

            refs.socket.on("disconnect", (reason) => {
                console.warn("‚ö†Ô∏è WebSocket disconnected:", reason);
                refs.socket = null;
            });
        }

        // ‚úÖ KEEP EVENT LISTENERS UNTIL AUTHORIZATION IS COMPLETE
        configureSocketListeners(refs.socket, state.currentState, {
            onData: handleDataEvent,
            onError: handleTimeout,
        });

        return () => {
            console.log("üõë Not cleaning up event listeners until authorization is complete...");
        };
    }, [state.currentState, handleTimeout, handleDataEvent]);

    const handleComplete = useCallback(async () => {
        if (refs.isSubmitting) return;
        refs.isSubmitting = true;

        console.log("üöÄ Checking state sequence...");

        const currentIndex = STATE_SEQUENCE.indexOf(state.currentState);
        if (currentIndex < STATE_SEQUENCE.length - 1) {
            updateState({
                currentState: STATE_SEQUENCE[currentIndex + 1],
                stabilityTime: 0,
            });

            refs.isSubmitting = false;
            return;
        }

        try {
            const faceId = localStorage.getItem("faceId");
            if (!faceId) throw new Error("‚ùå Face ID not found");

            console.log("üì° Sending final data...");

            // ‚úÖ Prevent WebSocket from being reset when navigating
            refs.hasNavigated = true;

            localStorage.setItem("results", JSON.stringify({
                temperature: state.temperatureData.temperature,
                alcohol: state.alcoholData.alcoholLevel,
            }));

            navigate("/complete-authentication", { state: { success: true } });

            setTimeout(() => {
                console.log("‚è≥ Navigating to home...");
                navigate("/");
            }, 4000);
        } catch (error) {
            console.error("‚ùå Submission error:", error);
            toast.error("–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ –¥–∞–Ω–Ω—ã—Ö. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ.");
            refs.isSubmitting = false;
        } finally {
            // ‚úÖ ONLY CLEAN UP WEBSOCKET IF NAVIGATION HAS COMPLETED
            if (refs.hasNavigated) {
                console.log("üõë Cleaning up event listeners after full authentication...");
                refs.socket?.off("temperature");
                refs.socket?.off("alcohol");
                refs.socket?.off("camera");
                refs.socket?.disconnect();
                refs.socket = null; // Ensure WebSocket is properly reset
            }
        }
    }, [state, navigate, updateState]);

    return {
        ...state,
        handleComplete,
        setCurrentState: (newState) =>
            updateState({ currentState: typeof newState === "function" ? newState(state.currentState) : newState }),
    };
};
