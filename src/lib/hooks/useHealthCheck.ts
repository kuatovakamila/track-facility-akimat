import { useState, useEffect, useCallback, useRef } from "react";
import { useNavigate } from "react-router-dom";
import { io, type Socket } from "socket.io-client";
import { StateKey } from "../constants";
import toast from "react-hot-toast";
import { ref, onValue } from "firebase/database";
import { db } from "./firebase";

const MAX_STABILITY_TIME = 7;
const SOCKET_TIMEOUT = 15000; // ‚úÖ Timeout for WebSocket connection

type SensorData = {
    temperature?: string;
    cameraStatus?: "failed" | "success";
};

type FirebaseAlcoholData = {
    power: number;
    sober: number;
    drunk: number;
    relay: number;
    ready: number;
    status: string;
};

type HealthCheckState = {
    currentState: StateKey;
    stabilityTime: number;
    temperatureData: { temperature: number };
    alcoholData: { alcoholLevel: string };
    secondsLeft: number;
};

const STATE_SEQUENCE: StateKey[] = ["TEMPERATURE", "ALCOHOL"];

export const useHealthCheck = (): HealthCheckState & {
    handleComplete: () => Promise<void>;
    setCurrentState: React.Dispatch<React.SetStateAction<StateKey>>;
} => {
    const navigate = useNavigate();
    const [state, setState] = useState<HealthCheckState>({
        currentState: "TEMPERATURE",
        stabilityTime: 0,
        temperatureData: { temperature: 0 },
        alcoholData: { alcoholLevel: "–ù–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ" },
        secondsLeft: 15,
    });

    const refs = useRef({
        socket: null as Socket | null,
        stopPolling: false,
        completed: false,
        hasTimedOut: false,
        timeout: null as NodeJS.Timeout | null,
    }).current;

    const updateState = useCallback(
        <K extends keyof HealthCheckState>(updates: Pick<HealthCheckState, K>) => {
            setState((prev) => ({ ...prev, ...updates }));
        },
        []
    );

    const handleTimeout = useCallback(() => {
        if (refs.hasTimedOut) return;
        refs.hasTimedOut = true;

        console.warn("‚è≥ Timeout reached: No valid alcohol data received.");
        navigate("/");
    }, [navigate]);

    const handleTemperatureData = useCallback(
        (data: SensorData) => {
            if (!data || !data.temperature) return;

            console.log("üì° Temperature data received:", data);

            setState((prev) => ({
                ...prev,
                stabilityTime: prev.currentState === "TEMPERATURE"
                    ? Math.min(prev.stabilityTime + 1, MAX_STABILITY_TIME)
                    : prev.stabilityTime,
                temperatureData: { temperature: Number(data.temperature) || 0 },
            }));
        },
        []
    );

    const pollAlcoholData = useCallback(() => {
        if (refs.stopPolling) return;

        const alcoholRef = ref(db, "alcohol_value");
        console.log("üîÑ Listening for alcohol data from Firebase...");

        const unsubscribe = onValue(alcoholRef, (snapshot) => {
            const data: FirebaseAlcoholData | null = snapshot.val();

            console.log("üî• Firebase Alcohol Data:", data);

            if (!data || typeof data !== "object") {
                console.warn("‚ö†Ô∏è No valid alcohol data received. Waiting...");
                return;
            }

            const sober = Number(data.sober ?? -1);
            const drunk = Number(data.drunk ?? -1);

            console.log(`üîç Extracted Values ‚Üí Sober: ${sober}, Drunk: ${drunk}`);

            let alcoholStatus = "–ù–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ";
            if (sober === 0) {
                alcoholStatus = "–¢—Ä–µ–∑–≤—ã–π";
            } else if (drunk === 0) {
                alcoholStatus = "–ü—å—è–Ω—ã–π";
            } else {
                console.warn("‚ö†Ô∏è No valid alcohol status yet. Still listening...");
                return;
            }

            console.log(`‚úÖ Alcohol Status Set: ${alcoholStatus}`);

            refs.stopPolling = true;
            setState((prev) => ({
                ...prev,
                stabilityTime: MAX_STABILITY_TIME,
                alcoholData: { alcoholLevel: alcoholStatus },
            }));

            setTimeout(handleComplete, 300);
            unsubscribe(); // ‚úÖ Stop listening once valid data is received
        });

        return () => {
            console.log("üõë Unsubscribing from Firebase updates.");
            unsubscribe();
        };
    }, []);

    useEffect(() => {
        if (!refs.socket) {
            refs.socket = io(import.meta.env.VITE_SERVER_URL || "http://localhost:3001", {
                transports: ["websocket"],
                reconnection: true,
                reconnectionAttempts: 5,
                reconnectionDelay: 3000,
            });

            refs.socket.on("connect", () => {
                console.log("‚úÖ WebSocket connected.");
                refs.timeout = setTimeout(handleTimeout, SOCKET_TIMEOUT); // ‚úÖ Start timeout
            });

            refs.socket.on("disconnect", (reason) => {
                console.warn("‚ö†Ô∏è WebSocket disconnected:", reason);
                refs.socket = null;
            });

            refs.socket.on("temperature", handleTemperatureData);
        }

        if (state.currentState === "ALCOHOL") {
            pollAlcoholData();
        }

        return () => {
            console.log("üõë Cleanup: Disconnect WebSocket & Stop Polling");
            refs.socket?.disconnect();
            refs.socket = null;
            refs.stopPolling = true;
            clearTimeout(refs.timeout!); // ‚úÖ Clear timeout
        };
    }, [state.currentState, handleTemperatureData, pollAlcoholData]);

    const handleComplete = useCallback(async () => {
        if (refs.completed) return;
        refs.completed = true;

        console.log("üöÄ Checking state sequence...");

        const currentIndex = STATE_SEQUENCE.indexOf(state.currentState);

        if (currentIndex < STATE_SEQUENCE.length - 1) {
            updateState({
                currentState: STATE_SEQUENCE[currentIndex + 1],
                stabilityTime: 0,
            });

            refs.completed = false;
            return;
        }

        console.log("‚úÖ Completing authentication after ALCOHOL");

        try {
            refs.socket?.disconnect();
            refs.socket = null;

            const faceId = localStorage.getItem("faceId");
            if (!faceId) throw new Error("‚ùå Face ID not found");

            console.log("üì° Sending final data...");

            localStorage.setItem("results", JSON.stringify({
                temperature: state.temperatureData.temperature,
                alcohol: state.alcoholData.alcoholLevel,
            }));

            refs.stopPolling = true;

            navigate("/complete-authentication", { state: { success: true } });
        } catch (error) {
            console.error("‚ùå Submission error:", error);
            toast.error("–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ –¥–∞–Ω–Ω—ã—Ö. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ.");
            refs.completed = false;
        }
    }, [state, navigate]);

    const setCurrentState = (newState: React.SetStateAction<StateKey>) => {
        updateState({
            currentState: typeof newState === "function" ? newState(state.currentState) : newState,
        });
    };

    return { ...state, handleComplete, setCurrentState };
};
